    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
        }

        private async void LoadDirectoryButton_Click(object sender, RoutedEventArgs e)
        {
            var folderPicker = new FolderPicker();
            folderPicker.SuggestedStartLocation = PickerLocationId.Desktop;
            folderPicker.FileTypeFilter.Add("*");

            StorageFolder folder = await folderPicker.PickSingleFolderAsync();
            if (folder != null)
            {
                // Set the text of the text boxes
                DirectoryNameTextBox.Text = folder.DisplayName;
                DirectoryPathTextBox.Text = folder.Path;

                // Optionally process files in the directory
                // var files = await folder.GetFilesAsync();
                // foreach (StorageFile file in files)
                // {
                //     // Do something with each file
                // }
            }
            else
            {
                // The user cancelled the picking operation
                // Optionally clear the text boxes
                DirectoryNameTextBox.Text = "";
                DirectoryPathTextBox.Text = "";
            }
        }
    }
}
